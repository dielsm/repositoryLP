    private void pop() {
        TableModel mod = jTable2.getModel();
        try {
            full = jTable2.getValueAt(9, 0).toString();
        } catch (Exception ex) {
        }
        if (full.length() != 0) {
            um = jTable2.getValueAt(0, 0).toString();
            dois = jTable2.getValueAt(1, 0).toString();
            tres = jTable2.getValueAt(2, 0).toString();
            quatro = jTable2.getValueAt(3, 0).toString();
            cinco = jTable2.getValueAt(4, 0).toString();
            seis = jTable2.getValueAt(5, 0).toString();
            sete = jTable2.getValueAt(6, 0).toString();
            oito = jTable2.getValueAt(7, 0).toString();
            nove = jTable2.getValueAt(8, 0).toString();

            mod.setValueAt(jTextArea1.getText(), 0, 0);
            mod.setValueAt(um, 1, 0);
            mod.setValueAt(dois, 2, 0);
            mod.setValueAt(tres, 3, 0);
            mod.setValueAt(quatro, 4, 0);
            mod.setValueAt(cinco, 5, 0);
            mod.setValueAt(seis, 6, 0);
            mod.setValueAt(sete, 7, 0);
            mod.setValueAt(oito, 8, 0);
            mod.setValueAt(nove, 9, 0);
        } else {
            mod.setValueAt(jTextArea1.getText(), line, 0);
            line++;
        }
    }
